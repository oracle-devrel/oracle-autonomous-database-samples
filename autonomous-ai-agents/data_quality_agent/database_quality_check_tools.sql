  ) RETURN CLOB IS
    l_null_rate   NUMBER := 0;
    l_dup_rate    NUMBER := 0;
    l_out_rate    NUMBER := 0;
    l_drift_score NUMBER := 0;
    l_score       NUMBER := 100;
    l_sev_rank    NUMBER := 1;
    l_worst_sev   VARCHAR2(20) := 'LOW';
    l_run_id      NUMBER;
    l_obj         JSON_OBJECT_T := JSON_OBJECT_T();
    l_sql         VARCHAR2(32767);
    l_hist_avg    NUMBER := 0;
    l_curr_avg    NUMBER := 0;
    l_drift_pct   NUMBER := 0;
  BEGIN
    ensure_internal_tables;

    BEGIN
      l_sql := 'SELECT AVG(issue_value) FROM DQ_FINDINGS$ WHERE owner_name = UPPER(:1) AND table_name = UPPER(:2) AND issue_type = ''NULL_RATE'' AND status = ''OPEN''';
      EXECUTE IMMEDIATE l_sql INTO l_null_rate USING owner_name, table_name;
    EXCEPTION
      WHEN OTHERS THEN l_null_rate := 0;
    END;

    BEGIN
      l_sql := 'SELECT AVG(issue_value) FROM DQ_FINDINGS$ WHERE owner_name = UPPER(:1) AND table_name = UPPER(:2) AND issue_type = ''DUPLICATE_RATE'' AND status = ''OPEN''';
      EXECUTE IMMEDIATE l_sql INTO l_dup_rate USING owner_name, table_name;
    EXCEPTION
      WHEN OTHERS THEN l_dup_rate := 0;
    END;

    BEGIN
      l_sql := 'SELECT AVG(issue_value) FROM DQ_FINDINGS$ WHERE owner_name = UPPER(:1) AND table_name = UPPER(:2) AND issue_type = ''OUTLIER_RATE'' AND status = ''OPEN''';
      EXECUTE IMMEDIATE l_sql INTO l_out_rate USING owner_name, table_name;
    EXCEPTION
      WHEN OTHERS THEN l_out_rate := 0;
    END;

    BEGIN
      l_sql := 'SELECT MAX(severity) KEEP (DENSE_RANK LAST ORDER BY CASE severity WHEN ''LOW'' THEN 1 WHEN ''MEDIUM'' THEN 2 WHEN ''HIGH'' THEN 3 ELSE 4 END) FROM DQ_FINDINGS$ WHERE owner_name = UPPER(:1) AND table_name = UPPER(:2) AND status = ''OPEN''';
      EXECUTE IMMEDIATE l_sql INTO l_worst_sev USING owner_name, table_name;
    EXCEPTION
      WHEN OTHERS THEN l_worst_sev := 'LOW';
    END;

    l_null_rate := NVL(l_null_rate, 0);
    l_dup_rate := NVL(l_dup_rate, 0);
    l_out_rate := NVL(l_out_rate, 0);
    BEGIN
      l_sql := q'[
        SELECT NVL(AVG(quality_score), 0)
        FROM DQ_RUN_HISTORY$
        WHERE owner_name = UPPER(:1)
          AND table_name = UPPER(:2)
          AND run_ts >= SYSTIMESTAMP - NUMTODSINTERVAL(30, 'DAY')
          AND run_ts < SYSTIMESTAMP - NUMTODSINTERVAL(7, 'DAY')
      ]';
      EXECUTE IMMEDIATE l_sql INTO l_hist_avg USING owner_name, table_name;

      l_sql := q'[
        SELECT NVL(AVG(quality_score), 0)
        FROM DQ_RUN_HISTORY$
        WHERE owner_name = UPPER(:1)
          AND table_name = UPPER(:2)
          AND run_ts >= SYSTIMESTAMP - NUMTODSINTERVAL(7, 'DAY')
      ]';
      EXECUTE IMMEDIATE l_sql INTO l_curr_avg USING owner_name, table_name;

      IF l_hist_avg > 0 THEN
        l_drift_pct := ABS(ROUND(((l_curr_avg - l_hist_avg) / l_hist_avg) * 100, 4));
      ELSE
        l_drift_pct := 0;
      END IF;
    EXCEPTION
      WHEN OTHERS THEN
        l_drift_pct := 0;
    END;
    l_drift_score := LEAST(l_drift_pct, 100);

    l_score := GREATEST(0, ROUND(100 - (l_null_rate * 0.35) - (l_dup_rate * 0.35) - (l_out_rate * 0.20) - (l_drift_score * 0.10), 2));

    l_sev_rank := severity_rank(l_worst_sev);
    l_worst_sev := severity_name(l_sev_rank);

    INSERT INTO DQ_RUN_HISTORY$(
      owner_name,
      table_name,
      run_ts,
      quality_score,
      null_rate_pct,
      duplicate_rate_pct,
      outlier_rate_pct,
      drift_score_pct,
      worst_severity,
      details
    ) VALUES (
      UPPER(owner_name),
      UPPER(table_name),
      SYSTIMESTAMP,
      l_score,
      l_null_rate,
      l_dup_rate,
      l_out_rate,
      l_drift_score,
      l_worst_sev,
      JSON_OBJECT('source' VALUE 'evaluate_quality_score v1')
    ) RETURNING run_id INTO l_run_id;

    l_obj.put('status', 'ok');
    l_obj.put('owner_name', UPPER(owner_name));
    l_obj.put('table_name', UPPER(table_name));
    l_obj.put('run_id', l_run_id);
    l_obj.put('quality_score', l_score);
    l_obj.put('worst_severity', l_worst_sev);
    l_obj.put('components', JSON_OBJECT(
      'null_rate_pct' VALUE l_null_rate,
      'duplicate_rate_pct' VALUE l_dup_rate,
      'outlier_rate_pct' VALUE l_out_rate,
      'drift_score_pct' VALUE l_drift_score
    ));
    RETURN l_obj.to_clob;
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('evaluate_quality_score failed: ' || SQLERRM));
  END evaluate_quality_score;

