CREATE OR REPLACE PACKAGE BODY database_quality AS
  FUNCTION quote_ident(p_name IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN DBMS_ASSERT.ENQUOTE_NAME(UPPER(TRIM(p_name)), FALSE);
  END quote_ident;

  FUNCTION full_table_name(p_owner IN VARCHAR2, p_table IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN quote_ident(p_owner) || '.' || quote_ident(p_table);
  END full_table_name;

  FUNCTION severity_rank(p_severity IN VARCHAR2) RETURN NUMBER IS
  BEGIN
    RETURN CASE UPPER(NVL(TRIM(p_severity), 'LOW'))
      WHEN 'CRITICAL' THEN 4
      WHEN 'HIGH' THEN 3
      WHEN 'MEDIUM' THEN 2
      ELSE 1
    END;
  END severity_rank;

  FUNCTION severity_name(p_rank IN NUMBER) RETURN VARCHAR2 IS
  BEGIN
    RETURN CASE
      WHEN p_rank >= 4 THEN 'CRITICAL'
      WHEN p_rank = 3 THEN 'HIGH'
      WHEN p_rank = 2 THEN 'MEDIUM'
      ELSE 'LOW'
    END;
  END severity_name;

  FUNCTION get_cfg_value(p_key IN VARCHAR2) RETURN CLOB IS
    l_val CLOB;
  BEGIN
    SELECT value
      INTO l_val
      FROM SELECTAI_AGENT_CONFIG
     WHERE UPPER(agent) = 'DATA_QUALITY'
       AND UPPER(key) = UPPER(p_key);
    RETURN l_val;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RETURN NULL;
  END get_cfg_value;

  FUNCTION is_safe_remediation_sql(
    p_sql         IN CLOB,
    p_owner_name  IN VARCHAR2,
    p_table_name  IN VARCHAR2
  ) RETURN BOOLEAN IS
    l_sql CLOB := UPPER(TRIM(p_sql));
    l_target VARCHAR2(300) := UPPER(TRIM(p_owner_name)) || '.' || UPPER(TRIM(p_table_name));
  BEGIN
    IF l_sql IS NULL THEN
      RETURN FALSE;
    END IF;

    IF INSTR(l_sql, ';') > 0 OR INSTR(l_sql, '/*') > 0 OR INSTR(l_sql, '--') > 0 THEN
      RETURN FALSE;
    END IF;

    IF NOT (l_sql LIKE 'UPDATE %' OR l_sql LIKE 'DELETE %' OR l_sql LIKE 'MERGE %') THEN
      RETURN FALSE;
    END IF;

    IF l_sql LIKE '%DROP %' OR l_sql LIKE '%TRUNCATE %' OR l_sql LIKE '%ALTER %' OR
       l_sql LIKE '%CREATE %' OR l_sql LIKE '%GRANT %' THEN
      RETURN FALSE;
    END IF;

    IF INSTR(l_sql, l_target) = 0 THEN
      RETURN FALSE;
    END IF;

    RETURN TRUE;
  END is_safe_remediation_sql;

