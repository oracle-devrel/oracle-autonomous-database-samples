        ' SET <column_name> = <default_value> WHERE <column_name> IS NULL')
    ));

    l_arr.append(JSON_OBJECT(
      'issue_type' VALUE 'DUPLICATE_RATE',
      'action' VALUE 'Deduplicate using row_number over key columns and keep latest record.',
      'sql_template' VALUE ('DELETE FROM ' || UPPER(owner_name) || '.' || UPPER(table_name) ||
        ' t WHERE ROWID IN (SELECT rid FROM (SELECT ROWID rid, ROW_NUMBER() OVER (PARTITION BY <key_columns> ORDER BY <updated_col> DESC) rn FROM ' ||
        UPPER(owner_name) || '.' || UPPER(table_name) || ') WHERE rn > 1)')
    ));

    l_arr.append(JSON_OBJECT(
      'issue_type' VALUE 'OUTLIER_RATE',
      'action' VALUE 'Cap or isolate outliers based on statistical bounds and business rules.',
      'sql_template' VALUE ('SELECT * FROM ' || UPPER(owner_name) || '.' || UPPER(table_name) ||
        ' WHERE ABS((<metric_col> - <mean>) / NULLIF(<stddev>,0)) > 3')
    ));

    l_obj.put('status', 'ok');
    l_obj.put('owner_name', UPPER(owner_name));
    l_obj.put('table_name', UPPER(table_name));
    l_obj.put('recommendations', l_arr);
    RETURN l_obj.to_clob;
  END suggest_remediation;

  FUNCTION apply_remediation(
    p_owner_name   IN VARCHAR2,
    p_table_name   IN VARCHAR2,
    p_issue_id     IN NUMBER,
    p_execute_mode IN VARCHAR2 DEFAULT 'PREVIEW',
    p_approval_code IN VARCHAR2 DEFAULT NULL
  ) RETURN CLOB IS
    l_sql      CLOB;
    l_mode     VARCHAR2(30) := UPPER(NVL(p_execute_mode, 'PREVIEW'));
    l_required_approval CLOB;
    l_rows     NUMBER := 0;
    l_savepoint_set BOOLEAN := FALSE;
  BEGIN
    ensure_internal_tables;

    SELECT generated_fix_sql
      INTO l_sql
      FROM DQ_FINDINGS$
      WHERE issue_id = p_issue_id
        AND owner_name = UPPER(p_owner_name)
        AND table_name = UPPER(p_table_name);

    IF l_mode = 'APPLY' THEN
      l_required_approval := get_cfg_value('REMEDIATION_APPROVAL_CODE');
      IF NVL(TRIM(l_required_approval), 'APPROVED') <> NVL(TRIM(p_approval_code), 'NO_APPROVAL') THEN
        RETURN JSON_OBJECT(
                 'status' VALUE 'error',
                 'execute_mode' VALUE l_mode,
                 'message' VALUE 'Approval code mismatch. Execution blocked.',
                 'hint' VALUE 'Set SELECTAI_AGENT_CONFIG key REMEDIATION_APPROVAL_CODE for AGENT=DATA_QUALITY, then pass matching approval code.'
               );
      END IF;

      IF l_sql IS NULL THEN
        RETURN JSON_OBJECT('status' VALUE 'error',
                           'message' VALUE 'No generated_fix_sql available for this issue.');
      END IF;

      IF NOT is_safe_remediation_sql(l_sql, p_owner_name, p_table_name) THEN
        RETURN JSON_OBJECT(
                 'status' VALUE 'error',
                 'execute_mode' VALUE l_mode,
                 'message' VALUE 'Unsafe remediation SQL blocked by policy.',
                 'policy' VALUE 'Only single-statement UPDATE/DELETE/MERGE on target table is allowed; DDL/comments/multi-statement are blocked.',
                 'preview_sql' VALUE l_sql
               );
      END IF;

      SAVEPOINT dq_apply_start;
      l_savepoint_set := TRUE;
      EXECUTE IMMEDIATE l_sql;
      l_rows := SQL%ROWCOUNT;

      UPDATE DQ_FINDINGS$
         SET status = 'RESOLVED'
       WHERE issue_id = p_issue_id;

      RETURN JSON_OBJECT('status' VALUE 'ok',
                         'execute_mode' VALUE l_mode,
                         'rows_affected' VALUE l_rows,
                         'message' VALUE 'Remediation applied successfully.');
    END IF;

    RETURN JSON_OBJECT('status' VALUE 'ok',
                       'execute_mode' VALUE l_mode,
                       'preview_sql' VALUE l_sql,
                       'message' VALUE 'Preview mode only. Re-run with execute_mode=APPLY and approval_code to execute.');
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE 'Issue not found for the given table/scope.');
    WHEN OTHERS THEN
      IF l_savepoint_set THEN
        ROLLBACK TO dq_apply_start;
      END IF;
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('apply_remediation failed: ' || SQLERRM));
  END apply_remediation;

  FUNCTION setup_oml_data_monitoring(
    p_monitor_name IN VARCHAR2,
    p_target_schema IN VARCHAR2,
    p_target_table IN VARCHAR2,
    p_baseline_expr IN VARCHAR2,
    p_newdata_expr IN VARCHAR2
  ) RETURN CLOB IS
    l_endpoint CLOB;
    l_cred     CLOB;
    l_uri      VARCHAR2(4000);
    l_body     CLOB;
    l_resp     DBMS_CLOUD_TYPES.resp;
    l_resp_txt CLOB;
    l_job_id   VARCHAR2(256);
  BEGIN
    ensure_internal_tables;
    l_endpoint := get_cfg_value('OML_MONITORING_ENDPOINT');
    l_cred := get_cfg_value('OML_MONITORING_CREDENTIAL');

    IF l_endpoint IS NULL OR l_cred IS NULL THEN
      RETURN JSON_OBJECT(
               'status' VALUE 'error',
               'message' VALUE 'OML monitoring config missing.',
               'required_keys' VALUE JSON_ARRAY('OML_MONITORING_ENDPOINT', 'OML_MONITORING_CREDENTIAL'),
               'agent' VALUE 'DATA_QUALITY'
             );
    END IF;

    l_uri := RTRIM(l_endpoint, '/') || '/omlmod/v1/jobs';
    l_body := JSON_OBJECT(
      'jobName' VALUE p_monitor_name,
      'jobType' VALUE 'DATA_MONITORING',
      'baselineDataQuery' VALUE p_baseline_expr,
      'newDataQuery' VALUE p_newdata_expr,
      'targetTable' VALUE UPPER(p_target_schema) || '.' || UPPER(p_target_table)
    );

    l_resp := DBMS_CLOUD.SEND_REQUEST(
      credential_name => l_cred,
      uri             => l_uri,
      method          => DBMS_CLOUD.METHOD_POST,
      body            => UTL_RAW.CAST_TO_RAW(l_body)
    );
    l_resp_txt := DBMS_CLOUD.GET_RESPONSE_TEXT(l_resp);

    BEGIN
      l_job_id := JSON_VALUE(l_resp_txt, '$.jobId');
    EXCEPTION
      WHEN OTHERS THEN
        l_job_id := NULL;
    END;

    MERGE INTO DQ_OML_MONITORS$ t
    USING (
      SELECT UPPER(p_monitor_name) monitor_name FROM dual
    ) s
    ON (t.monitor_name = s.monitor_name)
    WHEN MATCHED THEN
      UPDATE SET
        t.target_schema = UPPER(p_target_schema),
        t.target_table = UPPER(p_target_table),
        t.baseline_expr = p_baseline_expr,
        t.newdata_expr = p_newdata_expr,
        t.oml_job_id = l_job_id,
        t.status = 'REGISTERED',
        t.last_run_ts = SYSTIMESTAMP,
        t.last_run_response = l_resp_txt
    WHEN NOT MATCHED THEN
      INSERT (monitor_name, target_schema, target_table, baseline_expr, newdata_expr, oml_job_id, status, last_run_ts, last_run_response)
      VALUES (UPPER(p_monitor_name), UPPER(p_target_schema), UPPER(p_target_table), p_baseline_expr, p_newdata_expr, l_job_id, 'REGISTERED', SYSTIMESTAMP, l_resp_txt);

    RETURN JSON_OBJECT(
             'status' VALUE 'ok',
             'monitor_name' VALUE UPPER(p_monitor_name),
             'job_id' VALUE l_job_id,
             'response' VALUE l_resp_txt
           );
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('setup_oml_data_monitoring failed: ' || SQLERRM));
  END setup_oml_data_monitoring;

  FUNCTION run_oml_data_monitoring(
    p_monitor_name IN VARCHAR2
  ) RETURN CLOB IS
    l_endpoint CLOB;
    l_cred     CLOB;
    l_job_id   VARCHAR2(256);
    l_uri      VARCHAR2(4000);
    l_resp     DBMS_CLOUD_TYPES.resp;
    l_resp_txt CLOB;
  BEGIN
    ensure_internal_tables;
    l_endpoint := get_cfg_value('OML_MONITORING_ENDPOINT');
    l_cred := get_cfg_value('OML_MONITORING_CREDENTIAL');

    IF l_endpoint IS NULL OR l_cred IS NULL THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE 'OML monitoring config missing in SELECTAI_AGENT_CONFIG for AGENT=DATA_QUALITY.');
    END IF;

    SELECT oml_job_id
      INTO l_job_id
      FROM DQ_OML_MONITORS$
     WHERE monitor_name = UPPER(p_monitor_name);

    IF l_job_id IS NULL THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE 'No OML job id mapped for this monitor. Run setup first.');
    END IF;

    l_uri := RTRIM(l_endpoint, '/') || '/omlmod/v1/jobs/' || UTL_URL.ESCAPE(l_job_id, TRUE) || '/run';
    l_resp := DBMS_CLOUD.SEND_REQUEST(
      credential_name => l_cred,
      uri             => l_uri,
      method          => DBMS_CLOUD.METHOD_POST
    );
    l_resp_txt := DBMS_CLOUD.GET_RESPONSE_TEXT(l_resp);

    UPDATE DQ_OML_MONITORS$
       SET status = 'RUN_TRIGGERED',
           last_run_ts = SYSTIMESTAMP,
           last_run_response = l_resp_txt
     WHERE monitor_name = UPPER(p_monitor_name);

    RETURN JSON_OBJECT(
             'status' VALUE 'ok',
             'monitor_name' VALUE UPPER(p_monitor_name),
             'job_id' VALUE l_job_id,
             'response' VALUE l_resp_txt
           );
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE 'Monitor not found. Run setup_oml_data_monitoring first.');
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('run_oml_data_monitoring failed: ' || SQLERRM));
  END run_oml_data_monitoring;
END database_quality;
