  FUNCTION suggest_remediation(
    owner_name IN VARCHAR2,
    table_name IN VARCHAR2
  ) RETURN CLOB IS
    l_arr JSON_ARRAY_T := JSON_ARRAY_T();
    l_obj JSON_OBJECT_T := JSON_OBJECT_T();
