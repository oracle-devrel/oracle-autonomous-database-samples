  FUNCTION profile_table(
    p_owner_name  IN VARCHAR2,
    p_table_name  IN VARCHAR2,
    sample_rows IN NUMBER DEFAULT 10000
  ) RETURN CLOB IS
    l_sql         VARCHAR2(32767);
    l_row_count   NUMBER := 0;
    l_col_count   NUMBER := 0;
    l_resp        JSON_OBJECT_T := JSON_OBJECT_T();
  BEGIN
    ensure_internal_tables;

    l_sql := 'SELECT COUNT(*) FROM ' || full_table_name(p_owner_name, p_table_name);
    EXECUTE IMMEDIATE l_sql INTO l_row_count;

    SELECT COUNT(*)
      INTO l_col_count
      FROM ALL_TAB_COLUMNS
     WHERE OWNER = UPPER(p_owner_name)
       AND TABLE_NAME = UPPER(p_table_name);

    l_resp.put('status', 'ok');
    l_resp.put('owner_name', UPPER(p_owner_name));
    l_resp.put('table_name', UPPER(p_table_name));
    l_resp.put('sample_rows_requested', NVL(sample_rows, 10000));
    l_resp.put('row_count', l_row_count);
    l_resp.put('column_count', l_col_count);
    l_resp.put('message', 'Table profiling completed.');
    RETURN l_resp.to_clob;
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('profile_table failed: ' || SQLERRM));
  END profile_table;

  FUNCTION detect_nulls(
    p_owner_name IN VARCHAR2,
    p_table_name IN VARCHAR2
  ) RETURN CLOB IS
    l_sql       VARCHAR2(32767);
    l_row_count NUMBER := 0;
    l_obj       JSON_OBJECT_T := JSON_OBJECT_T();
    l_arr       JSON_ARRAY_T := JSON_ARRAY_T();
    l_col       VARCHAR2(128);
    l_nulls     NUMBER;
    l_rate      NUMBER;
  BEGIN
    ensure_internal_tables;

    l_sql := 'SELECT COUNT(*) FROM ' || full_table_name(p_owner_name, p_table_name);
    EXECUTE IMMEDIATE l_sql INTO l_row_count;

    FOR r IN (
      SELECT COLUMN_NAME
      FROM ALL_TAB_COLUMNS
      WHERE OWNER = UPPER(p_owner_name)
        AND TABLE_NAME = UPPER(p_table_name)
      ORDER BY COLUMN_ID
    ) LOOP
      l_col := quote_ident(r.COLUMN_NAME);
      l_sql := 'SELECT COUNT(*) FROM ' || full_table_name(p_owner_name, p_table_name) ||
               ' WHERE ' || l_col || ' IS NULL';
      EXECUTE IMMEDIATE l_sql INTO l_nulls;

      l_rate := CASE WHEN l_row_count = 0 THEN 0 ELSE ROUND((l_nulls / l_row_count) * 100, 4) END;

      IF l_nulls > 0 THEN
        l_arr.append(JSON_OBJECT(
          'column_name' VALUE r.COLUMN_NAME,
          'null_count' VALUE l_nulls,
          'null_rate_pct' VALUE l_rate,
          'severity' VALUE CASE WHEN l_rate >= 20 THEN 'HIGH' WHEN l_rate >= 5 THEN 'MEDIUM' ELSE 'LOW' END
        ));
      END IF;
    END LOOP;

    l_obj.put('status', 'ok');
    l_obj.put('owner_name', UPPER(p_owner_name));
    l_obj.put('table_name', UPPER(p_table_name));
    l_obj.put('row_count', l_row_count);
    l_obj.put('issues', l_arr);
    RETURN l_obj.to_clob;
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('detect_nulls failed: ' || SQLERRM));
  END detect_nulls;

  FUNCTION detect_duplicates(
    p_owner_name      IN VARCHAR2,
    p_table_name      IN VARCHAR2,
    p_key_columns_csv IN VARCHAR2 DEFAULT NULL
  ) RETURN CLOB IS
    l_sql           VARCHAR2(32767);
    l_rows          NUMBER := 0;
    l_distinct_rows NUMBER := 0;
    l_dup_rows      NUMBER := 0;
    l_rate          NUMBER := 0;
    l_obj           JSON_OBJECT_T := JSON_OBJECT_T();
  BEGIN
    ensure_internal_tables;

    l_sql := 'SELECT COUNT(*) FROM ' || full_table_name(p_owner_name, p_table_name);
    EXECUTE IMMEDIATE l_sql INTO l_rows;

    IF p_key_columns_csv IS NULL OR TRIM(p_key_columns_csv) IS NULL THEN
      l_sql := 'SELECT COUNT(*) FROM (SELECT DISTINCT * FROM ' ||
               full_table_name(p_owner_name, p_table_name) || ')';
    ELSE
      l_sql := 'SELECT COUNT(*) FROM (SELECT DISTINCT ' || p_key_columns_csv ||
               ' FROM ' || full_table_name(p_owner_name, p_table_name) || ')';
    END IF;

    EXECUTE IMMEDIATE l_sql INTO l_distinct_rows;

    l_dup_rows := GREATEST(l_rows - l_distinct_rows, 0);
    l_rate := CASE WHEN l_rows = 0 THEN 0 ELSE ROUND((l_dup_rows / l_rows) * 100, 4) END;

    l_obj.put('status', 'ok');
    l_obj.put('owner_name', UPPER(p_owner_name));
    l_obj.put('table_name', UPPER(p_table_name));
    l_obj.put('row_count', l_rows);
    l_obj.put('distinct_row_count', l_distinct_rows);
    l_obj.put('duplicate_row_count', l_dup_rows);
    l_obj.put('duplicate_rate_pct', l_rate);
    l_obj.put('severity', CASE WHEN l_rate >= 10 THEN 'HIGH' WHEN l_rate >= 2 THEN 'MEDIUM' ELSE 'LOW' END);
    RETURN l_obj.to_clob;
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('detect_duplicates failed: ' || SQLERRM));
  END detect_duplicates;

  FUNCTION detect_outliers(
    p_owner_name IN VARCHAR2,
    p_table_name IN VARCHAR2,
    p_z_limit    IN NUMBER DEFAULT 3
  ) RETURN CLOB IS
    l_obj         JSON_OBJECT_T := JSON_OBJECT_T();
    l_arr         JSON_ARRAY_T := JSON_ARRAY_T();
    l_sql         VARCHAR2(32767);
    l_outlier_cnt NUMBER;
    l_row_count   NUMBER;
    l_rate        NUMBER;
  BEGIN
    ensure_internal_tables;

    l_sql := 'SELECT COUNT(*) FROM ' || full_table_name(p_owner_name, p_table_name);
    EXECUTE IMMEDIATE l_sql INTO l_row_count;

    FOR c IN (
      SELECT COLUMN_NAME
      FROM ALL_TAB_COLUMNS
      WHERE OWNER = UPPER(p_owner_name)
        AND TABLE_NAME = UPPER(p_table_name)
        AND DATA_TYPE IN ('NUMBER', 'FLOAT', 'BINARY_DOUBLE', 'BINARY_FLOAT')
      ORDER BY COLUMN_ID
    ) LOOP
      l_sql :=
        'WITH s AS (' ||
        '  SELECT ' || quote_ident(c.COLUMN_NAME) || ' v FROM ' || full_table_name(p_owner_name, p_table_name) ||
        '  WHERE ' || quote_ident(c.COLUMN_NAME) || ' IS NOT NULL' ||
        '), m AS (' ||
        '  SELECT AVG(v) mu, STDDEV(v) sigma FROM s' ||
        ')' ||
        ' SELECT COUNT(*) FROM s, m' ||
        ' WHERE m.sigma > 0 AND ABS((s.v - m.mu) / m.sigma) > :z';

      EXECUTE IMMEDIATE l_sql INTO l_outlier_cnt USING NVL(p_z_limit, 3);
      l_rate := CASE WHEN l_row_count = 0 THEN 0 ELSE ROUND((l_outlier_cnt / l_row_count) * 100, 4) END;

      IF l_outlier_cnt > 0 THEN
        l_arr.append(JSON_OBJECT(
          'column_name' VALUE c.COLUMN_NAME,
          'outlier_count' VALUE l_outlier_cnt,
          'outlier_rate_pct' VALUE l_rate,
          'severity' VALUE CASE WHEN l_rate >= 10 THEN 'HIGH' WHEN l_rate >= 3 THEN 'MEDIUM' ELSE 'LOW' END
        ));
      END IF;
    END LOOP;

    l_obj.put('status', 'ok');
    l_obj.put('owner_name', UPPER(p_owner_name));
    l_obj.put('table_name', UPPER(p_table_name));
    l_obj.put('issues', l_arr);
    RETURN l_obj.to_clob;
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('detect_outliers failed: ' || SQLERRM));
  END detect_outliers;

  FUNCTION detect_drift(
    p_owner_name      IN VARCHAR2,
    p_table_name      IN VARCHAR2,
    p_baseline_days   IN NUMBER DEFAULT 30,
    p_current_days    IN NUMBER DEFAULT 7,
    p_drift_threshold IN NUMBER DEFAULT 20
  ) RETURN CLOB IS
    l_hist_avg NUMBER := 0;
    l_curr     NUMBER := 0;
    l_change   NUMBER := 0;
    l_sql      VARCHAR2(32767);
    l_obj      JSON_OBJECT_T := JSON_OBJECT_T();
  BEGIN
    ensure_internal_tables;

    l_sql := q'[
      SELECT NVL(AVG(quality_score), 0)
      FROM DQ_RUN_HISTORY$
      WHERE owner_name = UPPER(:1)
        AND table_name = UPPER(:2)
        AND run_ts >= SYSTIMESTAMP - NUMTODSINTERVAL(:3, 'DAY')
        AND run_ts < SYSTIMESTAMP - NUMTODSINTERVAL(:4, 'DAY')
    ]';
    EXECUTE IMMEDIATE l_sql INTO l_hist_avg USING p_owner_name, p_table_name,
      NVL(p_baseline_days, 30), NVL(p_current_days, 7);

    l_sql := q'[
      SELECT NVL(AVG(quality_score), 0)
      FROM DQ_RUN_HISTORY$
      WHERE owner_name = UPPER(:1)
        AND table_name = UPPER(:2)
        AND run_ts >= SYSTIMESTAMP - NUMTODSINTERVAL(:3, 'DAY')
    ]';
    EXECUTE IMMEDIATE l_sql INTO l_curr USING p_owner_name, p_table_name,
      NVL(p_current_days, 7);

    IF l_hist_avg = 0 THEN
      l_change := 0;
    ELSE
      l_change := ROUND(((l_curr - l_hist_avg) / l_hist_avg) * 100, 4);
    END IF;

    l_obj.put('status', 'ok');
    l_obj.put('owner_name', UPPER(p_owner_name));
    l_obj.put('table_name', UPPER(p_table_name));
    l_obj.put('baseline_avg_score', l_hist_avg);
    l_obj.put('current_avg_score', l_curr);
    l_obj.put('drift_pct', l_change);
    l_obj.put('threshold_pct', NVL(p_drift_threshold, 20));
    l_obj.put('is_drift_flagged', CASE WHEN ABS(l_change) >= NVL(p_drift_threshold, 20) THEN 'true' ELSE 'false' END);
    RETURN l_obj.to_clob;
  EXCEPTION
    WHEN OTHERS THEN
      RETURN JSON_OBJECT('status' VALUE 'error',
                         'message' VALUE ('detect_drift failed: ' || SQLERRM));
  END detect_drift;

